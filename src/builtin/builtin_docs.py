def get_doc(func_name: str) -> str:
    """
    获取函数的中文文档

    Args:
        func_name: 函数名（中文或英文）

    Returns:
        中文文档字符串
    """
    return BUILTIN_DOCS.get(func_name, f"暂无{func_name}函数的文档")


def list_documented_functions() -> list:
    """列出所有有文档的函数"""
    return list(BUILTIN_DOCS.keys())
